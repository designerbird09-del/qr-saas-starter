interface QrTypeNavigationProps {
  onSelect?: (type: QrType) => void;
}

export function QrTypeNavigation({ onSelect }: QrTypeNavigationProps) {
  const { type, setType } = useBuilderStore();

  function handleClick(newType: QrType) {
    setType(newType);
    onSelect?.(newType);
  }

  return (
    <div className="sticky top-0 z-30 -mx-4 mb-6 border-b border-white/10 bg-black/80 px-4 py-4 backdrop-blur-xl sm:mx-0 sm:rounded-xl sm:border">
      <div className="grid grid-cols-6 gap-2 sm:grid-cols-8 lg:grid-cols-10">
        {QR_TYPES.map(({ type: t, label, Icon }) => {
          const active = type === t;
          return (
            <button
              key={t}
              onClick={() => handleClick(t)}
              title={label}
              className={`group relative flex flex-col items-center gap-1 rounded-xl px-2 py-2 text-center transition-all duration-200 ${
                active
                  ? "bg-gradient-to-br from-[#d4a574] via-[#c68b59] to-[#b87333] text-[#1a0f0a] shadow-[0_0_15px_rgba(200,140,100,0.25)]"
                  : "border border-white/10 bg-white/5 text-neutral-300 hover:border-[#b87333]/40 hover:text-[#d4a574]"
              }`}
            >
              <span className={`relative flex h-8 w-8 items-center justify-center transition-all duration-300 ${active ? "text-[#1a0f0a]" : "text-[#b87333] group-hover:drop-shadow-[0_0_6px_rgba(184,115,51,0.6)]"}`}>
                <Icon className="h-5 w-5" />
              </span>
              <span className="text-[10px] font-medium leading-tight sm:text-xs">{label}</span>
            </button>
          );
        })}
      </div>
    </div>
  );
}

